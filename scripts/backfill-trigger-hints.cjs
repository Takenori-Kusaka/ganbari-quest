// seed.ts を読み込んで triggerHint を追加
const seedPath = path.join(__dirname, '..', 'src', 'lib', 'server', 'db', 'seed.ts');
let content = fs.readFileSync(seedPath, 'utf-8');

let added = 0;
let notFound = 0;

for (const [name, hint] of Object.entries(HINTS)) {
	// sortOrder: s(), の直後に triggerHint を挿入
	// パターン: name: 'xxx', ... sortOrder: s(),\n\t\t\t},
	// triggerHint が既にある場合はスキップ
	const nameEscaped = name.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
	const regex = new RegExp(
		`(name: '${nameEscaped}',(?:(?!\\n\\t\\t\\t\\}).)*?sortOrder: s\\(\\),)`,
		's',
	);
	const match = content.match(regex);
	if (match) {
		// triggerHint が既にある場合はスキップ
		if (match[0].includes('triggerHint')) {
			continue;
		}
		content = content.replace(match[0], match[0] + `\n\t\t\t\ttriggerHint: '${hint}',`);
		added++;
	} else {
		console.warn(`NOT FOUND: ${name}`);
		notFound++;
	}
}

fs.writeFileSync(seedPath, content, 'utf-8');
console.log(`Added triggerHint to ${added} activities (${notFound} not found)`);
